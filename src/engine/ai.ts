function squareIndex(square: string): number {
  const file = square.charCodeAt(0) - 'a'.charCodeAt(0);
  const rank = parseInt(square[1], 10) - 1;
  // PST is indexed from rank 8 down to rank 1 (white's view)
  return (7 - rank) * 8 + file;
}

function evaluateBoard(chess: Chess): number {
  let score = 0;
  const board = chess.board();

  for (let r = 0; r < 8; r++) {
    for (let f = 0; f < 8; f++) {
      const piece = board[r][f];
      if (!piece) continue;
      const type = piece.type;
      const value = PIECE_VALUES[type] ?? 0;
      const rank = 7 - r;
      const idx = r * 8 + f;
      const pstIdx = piece.color === 'w' ? idx : (7 - r) * 8 + f;
      const pst = PST[type]?.[pstIdx] ?? 0;
      const pieceScore = value + pst;
      score += piece.color === 'w' ? pieceScore : -pieceScore;
    }
  }
  return score;
}

function minimax(
  chess: Chess,
  depth: number,
  alpha: number,
  beta: number,
  maximising: boolean,
): number {
  if (depth === 0 || chess.isGameOver()) {
    if (chess.isCheckmate()) {
      return maximising ? -Infinity : Infinity;
    }
    if (chess.isDraw() || chess.isStalemate()) return 0;
    return evaluateBoard(chess);
  }

  const moves = chess.moves({ verbose: true });

  if (maximising) {
    let best = -Infinity;
    for (const move of moves) {
      chess.move(move);
      best = Math.max(best, minimax(chess, depth - 1, alpha, beta, false));
      chess.undo();
      alpha = Math.max(alpha, best);
      if (beta <= alpha) break;
    }
    return best;
  } else {
    let best = Infinity;
    for (const move of moves) {
      chess.move(move);
      best = Math.min(best, minimax(chess, depth - 1, alpha, beta, true));
      chess.undo();
      beta = Math.min(beta, best);
      if (beta <= alpha) break;
    }
    return best;
  }
}

export function getBestMove(fen: string, depth: number = 3): Move | null {
  const chess = new Chess(fen);
  if (chess.isGameOver()) return null;

  const moves = chess.moves({ verbose: true });
  if (moves.length === 0) return null;

  // Shuffle to vary play when scores are equal
  moves.sort(() => Math.random() - 0.5);

  const isMaximising = chess.turn() === 'w';
  let bestMove = moves[0];
  let bestScore = isMaximising ? -Infinity : Infinity;

  for (const move of moves) {
    chess.move(move);
    const score = minimax(chess, depth - 1, -Infinity, Infinity, !isMaximising);
    chess.undo();
    if (isMaximising ? score > bestScore : score < bestScore) {
      bestScore = score;
      bestMove = move;
    }
  }

  return bestMove;
}
